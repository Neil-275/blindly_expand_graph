"""
Extraction functions for LLM responses produced by prompts defined in
prompt_list.py and prompt_list2.py.

Each function targets the exact output format described in its corresponding
prompt and returns a clean Python object (list, dict, bool, etc.).
"""

import re
import json
import ast
from typing import Optional


# ---------------------------------------------------------------------------
# prompt_list.py - system_prompt / user_prompt_template
# Output: Python list of three NL query strings, e.g.
#   ["Query 1?", "Query 2?", "Query 3?"]
# ---------------------------------------------------------------------------

def extract_nlg_queries(text: str) -> list[str]:
    """
    Extract the list of three natural-language paraphrases generated by
    ``system_prompt`` + ``user_prompt_template``.

    The LLM is instructed to return only a Python list literal, but may
    occasionally wrap it in a code fence.  We try ast.literal_eval first,
    then fall back to a regex scan.

    Returns a list of strings (may be empty on complete parse failure).
    """
    text = text.strip()

    # Strip markdown code fences if present
    fence_match = re.search(r"```(?:python)?\s*([\s\S]*?)```", text)
    if fence_match:
        text = fence_match.group(1).strip()

    # Try to parse a Python list literal directly
    try:
        result = ast.literal_eval(text)
        if isinstance(result, list):
            return [str(q) for q in result]
    except (ValueError, SyntaxError):
        pass

    # Fallback: extract strings between the outermost [ … ]
    bracket_match = re.search(r"\[([^\[\]]*(?:\"[^\"]*\"|'[^']*')[^\[\]]*)\]", text, re.DOTALL)
    if bracket_match:
        try:
            result = ast.literal_eval("[" + bracket_match.group(1) + "]")
            if isinstance(result, list):
                return [str(q) for q in result]
        except (ValueError, SyntaxError):
            pass

    # Last resort: collect all quoted strings in order
    queries = re.findall(r'"([^"]+)"|\'([^\']+)\'', text)
    return [a or b for a, b in queries]


# ---------------------------------------------------------------------------
# prompt_list.py - extract_relation_prompt / RELATION_EXTRACTION_SYSTEM_PROMPT
# Output lines (until <END>):
#   {relation/path (Score: 0.4)}: Explanation text
# ---------------------------------------------------------------------------

def extract_relations_with_scores(text: str) -> list[dict]:
    """
    Parse the output of ``extract_relation_prompt`` or
    ``RELATION_EXTRACTION_SYSTEM_PROMPT``.

    Each returned dict has keys:
        ``relation`` (str)  - full canonical relation name
        ``score``    (float) - relevance score 0.0 - 1.0
        ``explanation`` (str) - free-text explanation
    """
    # Truncate at <END> marker
    end_idx = text.find("<END>")
    if end_idx != -1:
        text = text[:end_idx]

    pattern = re.compile(
        r"\{([^(){}]+)\s+\(Score:\s*([0-9]*\.?[0-9]+)\)\}\s*:?\s*(.*?)(?=\{|$)",
        re.DOTALL,
    )

    results = []
    for match in pattern.finditer(text):
        relation = match.group(1).strip()
        try:
            score = float(match.group(2))
        except ValueError:
            score = 0.0
        explanation = match.group(3).strip().replace("\n", " ")
        results.append({"relation": relation, "score": score, "explanation": explanation})

    return results


# ---------------------------------------------------------------------------
# prompt_list.py - prompt_evaluate
# Output: {Yes} or {No}, optionally a second {answer} token
# ---------------------------------------------------------------------------

def extract_sufficiency_decision(text: str) -> dict:
    """
    Parse the output of ``prompt_evaluate``.

    Returns a dict with:
        ``is_sufficient`` (bool)
        ``answer``        (str | None) - extracted answer entity when {Yes}
        ``explanation``   (str)        - raw explanation text
    """
    # Look for {Yes} / {No} (case-insensitive)
    decision_match = re.search(r"\{(Yes|No)\}", text, re.IGNORECASE)
    is_sufficient = False
    if decision_match:
        is_sufficient = decision_match.group(1).lower() == "yes"

    # Extract the answer entity when present: {Some Answer}
    # Skip the first {Yes}/{No} token itself
    answer = None
    if is_sufficient:
        answer_matches = re.findall(r"\{([^{}]+)\}", text)
        # The first hit is "Yes"; the second (if any) is the actual answer
        content_answers = [m for m in answer_matches if m.lower() not in ("yes", "no")]
        if content_answers:
            answer = content_answers[-1].strip()

    # Strip trailing <END>
    explanation = text.replace("<END>", "").strip()

    return {
        "is_sufficient": is_sufficient,
        "answer": answer,
        "explanation": explanation,
    }


