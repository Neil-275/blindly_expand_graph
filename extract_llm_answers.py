# ---------------------------------------------------------------------------
# prompt_list.py - score_entity_candidates_prompt
# Output first non-empty line: comma-separated float scores
#   0.0, 1.0, 0.0, 0.0
# ---------------------------------------------------------------------------

def extract_entity_scores(text: str, num_entities: Optional[int] = None) -> list[float]:
    """
    Parse the output of ``score_entity_candidates_prompt``.

    The LLM outputs scores as a comma-separated list on the first relevant
    line, followed by an explanation paragraph.

    Args:
        text:          Raw LLM response string.
        num_entities:  Expected number of scores; used to validate the parse.

    Returns:
        List of floats.  Falls back to uniform scores when parsing fails and
        ``num_entities`` was provided.
    """
    # Try to find a line that looks like comma-separated numbers
    for line in text.split("\n"):
        line = line.strip()
        tokens = re.split(r"[,\s]+", line)
        try:
            scores = [float(t) for t in tokens if t]
            if num_entities is None or len(scores) == num_entities:
                return scores
        except ValueError:
            continue

    # Fallback: all numbers in the text (in order of appearance)
    scores = [float(m) for m in re.findall(r"\b\d+(?:\.\d+)?\b", text)]
    if scores:
        if num_entities and len(scores) == num_entities:
            return scores
        if num_entities is None:
            return scores

    # Ultimate fallback: uniform distribution
    if num_entities:
        return [1.0 / num_entities] * num_entities
    return []


# ---------------------------------------------------------------------------
# prompt_list2.py - decision_prompt
# Output: JSON object
#   {
#     "strategic_reasoning": "...",
#     "contributory_relations": {
#       "existing": ["..."],
#       "missing":  ["..."]
#     }
#   }
# ---------------------------------------------------------------------------

def extract_decision_json(text: str) -> dict:
    """
    Parse the JSON object returned by ``decision_prompt``.

    Returns the parsed dict, or a partial dict with an ``_parse_error`` key
    if JSON decoding fails.

    Expected keys:
        ``strategic_reasoning``   (str)  - brief explanation of the reasoning step
        ``contributory_relations`` (dict) with:
            ``existing``  (list[int]) - IDs extracted from relations selected from the context
            ``missing``   (list[str]) - NL descriptions of relations absent from context
    """
    text = text.strip()

    # Strip markdown code fences
    fence_match = re.search(r"```(?:json)?\s*([\s\S]*?)```", text)
    if fence_match:
        text = fence_match.group(1).strip()

    result = None
    
    # Attempt direct JSON parse
    try:
        result = json.loads(text)
    except json.JSONDecodeError:
        # Try to locate the outermost JSON object
        brace_match = re.search(r"\{[\s\S]*\}", text)
        if brace_match:
            try:
                result = json.loads(brace_match.group(0))
            except json.JSONDecodeError:
                pass

    if result is None:
        return {
            "_parse_error": "Could not decode JSON from decision_prompt response.",
            "_raw": text,
        }

    # Extract IDs from existing relations (only for the existing array)
    if (isinstance(result, dict) and 
        "contributory_relations" in result and
        isinstance(result["contributory_relations"], dict) and 
        "existing" in result["contributory_relations"] and
        isinstance(result["contributory_relations"]["existing"], list)):
        
        existing_ids = []
        for item in result["contributory_relations"]["existing"]:
            if isinstance(item, str):
                # Extract ID (number before the colon)
                match = re.match(r"(\d+):", item)
                if match:
                    existing_ids.append(int(match.group(1)))
                else:
                    # If no colon pattern, keep the original item
                    existing_ids.append(item)
            else:
                existing_ids.append(str(item))
        
        result["contributory_relations"]["existing"] = existing_ids

    return result


