# prompt_list2.py - entities_pruning_prompt
# Output: JSON list  ["ent_1", "ent_2", ..., "ent_k"]
# ---------------------------------------------------------------------------

def extract_pruned_entities(text: str) -> list[str]:
    """
    Parse the JSON list of entity names returned by ``entities_pruning_prompt``.

    Returns a list of entity name strings (may be empty on parse failure).
    """
    text = text.strip()

    # Strip markdown code fences
    fence_match = re.search(r"```(?:json)?\s*([\s\S]*?)```", text)
    if fence_match:
        text = fence_match.group(1).strip()

    # Direct JSON parse
    try:
        result = json.loads(text)
        if isinstance(result, list):
            return [str(e) for e in result]
    except json.JSONDecodeError:
        pass

    # Locate the first JSON array
    bracket_match = re.search(r"\[[\s\S]*?\]", text)
    if bracket_match:
        try:
            result = json.loads(bracket_match.group(0))
            if isinstance(result, list):
                return [str(e) for e in result]
        except json.JSONDecodeError:
            pass

    # Fallback: collect all quoted strings inside brackets
    items = re.findall(r'"([^"]+)"|\'([^\']+)\'', text)
    return [a or b for a, b in items]


# ---------------------------------------------------------------------------
# prompt_list2.py - GoG_answer_prompt
# Output: {YES}. Answer: ["entity_name"]   or   {NO}
# ---------------------------------------------------------------------------

def extract_gog_answer(text: str) -> dict:
    """
    Parse the output of ``GoG_answer_prompt``.

    Returns a dict with:
        ``found``    (bool)          True when the LLM answered {YES}
        ``entities`` (list[str])     answer entity names when found
        ``raw``      (str)           original response text
    """
    text_stripped = text.strip()

    found = bool(re.search(r"\{YES\}", text_stripped, re.IGNORECASE))

    entities: list[str] = []
    if found:
        # Try to extract the JSON list after "Answer:"
        answer_match = re.search(r"Answer\s*:\s*(\[[\s\S]*?\])", text_stripped, re.IGNORECASE)
        if answer_match:
            try:
                entities = json.loads(answer_match.group(1))
            except json.JSONDecodeError:
                # Fallback: grab quoted strings inside the brackets
                items = re.findall(r'"([^"]+)"|\'([^\']+)\'', answer_match.group(1))
                entities = [a or b for a, b in items]

    return {"found": found, "entities": entities, "raw": text_stripped}
